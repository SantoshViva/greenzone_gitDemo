package com.santosh.greenzone.model;

public class IvrPromptDetailsModel {

	private String promptId; 
	private String promptName;
	private String promptDesc;
	private String promptPath;
	
	
	public String getPromptId() {
		return promptId;
	}
	public void setPromptId(String promptId) {
		this.promptId = promptId;
	}
	public String getPromptName() {
		return promptName;
	}
	public void setPromptName(String promptName) {
		this.promptName = promptName;
	}
	public String getPromptDesc() {
		return promptDesc;
	}
	public void setPromptDesc(String promptDesc) {
		this.promptDesc = promptDesc;
	}
	public String getPromptPath() {
		return promptPath;
	}
	public void setPromptPath(String promptPath) {
		this.promptPath = promptPath;
	}
	
	
}
